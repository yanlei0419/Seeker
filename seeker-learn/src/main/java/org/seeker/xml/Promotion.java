package org.seeker.xml;

public class Promotion {
	private String PromotionType;
	private String PromotionSubtype;
	private String PromotionName;
	private String PromotionRule;
	private String EffectiveTime;
	private String ExpiryTime;
	private String BeginTime;
	private String EndTime;
	private String UsefulTime;
	private String Description;
	private String PubInfo;
	private String Precondition;
	private String PromotionId;
	private String AllowBooking;
	public String getPromotionType() {
		return PromotionType;
	}
	public void setPromotionType(String promotionType) {
		PromotionType = promotionType;
	}
	public String getPromotionSubtype() {
		return PromotionSubtype;
	}
	public void setPromotionSubtype(String promotionSubtype) {
		PromotionSubtype = promotionSubtype;
	}
	public String getPromotionName() {
		return PromotionName;
	}
	public void setPromotionName(String promotionName) {
		PromotionName = promotionName;
	}
	public String getPromotionRule() {
		return PromotionRule;
	}
	public void setPromotionRule(String promotionRule) {
		PromotionRule = promotionRule;
	}
	public String getEffectiveTime() {
		return EffectiveTime;
	}
	public void setEffectiveTime(String effectiveTime) {
		EffectiveTime = effectiveTime;
	}
	public String getExpiryTime() {
		return ExpiryTime;
	}
	public void setExpiryTime(String expiryTime) {
		ExpiryTime = expiryTime;
	}
	public String getBeginTime() {
		return BeginTime;
	}
	public void setBeginTime(String beginTime) {
		BeginTime = beginTime;
	}
	public String getEndTime() {
		return EndTime;
	}
	public void setEndTime(String endTime) {
		EndTime = endTime;
	}
	public String getUsefulTime() {
		return UsefulTime;
	}
	public void setUsefulTime(String usefulTime) {
		UsefulTime = usefulTime;
	}
	public String getDescription() {
		return Description;
	}
	public void setDescription(String description) {
		Description = description;
	}
	public String getPubInfo() {
		return PubInfo;
	}
	public void setPubInfo(String pubInfo) {
		PubInfo = pubInfo;
	}
	public String getPrecondition() {
		return Precondition;
	}
	public void setPrecondition(String precondition) {
		Precondition = precondition;
	}
	public String getPromotionId() {
		return PromotionId;
	}
	public void setPromotionId(String promotionId) {
		PromotionId = promotionId;
	}
	public String getAllowBooking() {
		return AllowBooking;
	}
	public void setAllowBooking(String allowBooking) {
		AllowBooking = allowBooking;
	}

}
